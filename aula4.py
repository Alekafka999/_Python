'''
Tipos int e float
int -> números inteiros, ou seja, números sem parte decimal. Exemplos: 7, -4, 0, 9876543210
float -> números reais, ou seja, números com parte decimal. Exemplos: 4.5, -0.1, 3.14, 2.0
o tipo int representa qualquer número inteiro, positivo ou negatibo. int sem sinal é considerado positivo.


float -> Número com ponto flutuante
O tipo float representa qualquer número real, ou seja, números com parte decimal. Exemplos: 7.0, -4.5, 0.0, 3.14159
O número float pode ser positivo ou negativo com ponto flutuante
float sem sinal é considerado positivo.

A função type mostra o tipo que o Python inferiu ao valor

'''

print(11) #int
print(-11) #int
print(0) #int

print(4.5, 3.90867) #float


print(type('Otávio')    )
print(type(-1)    )
print(type(0)    )
print(type('1.1'), type (-1.1), type(0.0)    )