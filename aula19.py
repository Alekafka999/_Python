'''
Operadores de comparação (relacionais)
OP      Significado         Exemplo
>       Maior               2 > 1
>=      Maior ou igual      2 >= 2
<       Menor               1 < 2
<=      Menor ou igual      2 <= 2
==      Igual               'a' == 'a'
!=      Diferente           'a' != 'b'
'''

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)

'''
Operadores de atribuição
=   Atribuição simples
+=  Atribuição aditiva (acumulativa)
-=  Atribuição subtrativa
*=  Atribuição multiplicativa
/=  Atribuição divisiva
//= Atribuição divisiva inteira
**= Atribuição exponencial
%=  Atribuição modular
'''

numero = 10
numero += 5
print(numero)

'''
Operadores lógicos
and, or, not, is
and -> Verdadeiro se ambas as condições forem verdadeiras
or -> Verdadeiro se uma das condições for verdadeira
not -> Inverte o valor da condição
is -> Verifica se o valor é verdadeiro ou falso
'''