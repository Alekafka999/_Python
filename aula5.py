'''
Tipo de dado bool (booleano - True ou False)
Ao questionar algo, a resposta pode ser sim ou não, verdadeiro ou falso, 1 ou 0.
Esse tipo de dado é representado em Python pelo tipo bool, que pode assumir apenas dois valores: True (verdadeiro) ou False (falso).
Existem vários operadores para QUESTIONAR
Dentre eles, o ==, que é um operador lógico que questiona se um valor é igual a outro valor.

'''


print(10 == 10)  # True
print(10 == 5)   # False
print('Python' == 'python')  # False

print(type(10 == 11))  # bool
print(type(10 == 10))  # bool